package ExerciciosAula34Static;

public class ConversaoDeUnidadesDeVolume {

	public static double converterLitroParaCm3 (double valor) {
		return valor * 1000;
	}
	
	public static double converterM3ParaLitro (double valor) {
		return valor * 1000;
	}
	
	public static double converterM3ParaPésCúbicos(double valor) {
		return valor * 35.32;
	}
	
	public static double converterM3ParaPolegadasCúbicas (double valor) {
		return valor * 231;
	}
	
	public static double converterGalãoAmericanoParaLitro (double valor) {
		return valor * 3.758;
	}
}
