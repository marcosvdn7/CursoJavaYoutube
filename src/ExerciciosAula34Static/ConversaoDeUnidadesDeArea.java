package ExerciciosAula34Static;

public class ConversaoDeUnidadesDeArea {

	public static double converterMetrosParaPés (double valor) {
		return valor * 10.76;
	}
	
	public static double converterPésParaCentimetros (double valor) {
		return valor * 929;
	}
	
	public static double converterMilhasParaAcres (double valor) {
		return valor * 640;
	}
	
	public static double converterAcresParaPés (double valor) {
		return valor * 43.560;
	}
}
